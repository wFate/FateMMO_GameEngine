enum class ClassType : uint8_t {
    Warrior = 0,
    Mage = 1,
    Archer = 2,
    Any = 255
};

enum class ResourceType : uint8_t {
    None = 0,   // No resource cost (auto-attacks, passives)
    Fury = 1,   // Warriors/Archers - generated by attacks
    Mana = 2    // Mages - regenerates passively
};

// Class definition data (loaded from config, replaces Unity ScriptableObjects)
struct ClassDefinition {
    ClassType classType = ClassType::Warrior;
    std::string displayName = "Warrior";

    // Base stats at level 1
    int baseMaxHP = 120;
    int baseMaxMP = 40;
    int baseStrength = 12;
    int baseVitality = 12;
    int baseIntelligence = 8;
    int baseDexterity = 4;
    int baseWisdom = 8;

    // Combat stats at level 1
    float baseHitRate = 4.0f;
    float attackRange = 1.0f;

    // Resource system
    ResourceType primaryResource = ResourceType::Fury;
    int baseMaxFury = 3;
    int furyPerTenLevels = 1;
    float furyPerBasicAttack = 0.5f;
    float furyPerCriticalHit = 1.0f;
    float furyPerDamageReceived = 0.2f; // Warriors: 0.2, Rangers/Mages: 0.0

    // Per-level gains
    float hpPerLevel = 8.0f;
    float mpPerLevel = 3.0f;
    float strPerLevel = 1.5f;
    float vitPerLevel = 1.5f;
    float intPerLevel = 0.5f;
    float dexPerLevel = 0.8f;
    float wisPerLevel = 0.5f;

    int getMaxFuryForLevel(int level) const {
        if (primaryResource != ResourceType::Fury) return 0;
        return baseMaxFury + (level / 10) * furyPerTenLevels;
    }

    bool usesFury() const { return primaryResource == ResourceType::Fury; }
    bool usesMana() const { return primaryResource == ResourceType::Mana; }
};

