// ============================================================================
// Rarity Colors (matching TWOM style)
// ============================================================================
namespace RarityColors {
    inline Color getColor(ItemRarity rarity) {
        switch (rarity) {
            case ItemRarity::Common:    return Color::white();
            case ItemRarity::Uncommon:  return {0.290f, 0.871f, 0.502f};
            case ItemRarity::Rare:      return {0.376f, 0.647f, 0.980f};
            case ItemRarity::Epic:      return {0.659f, 0.333f, 0.969f};
            case ItemRarity::Legendary: return {0.984f, 0.573f, 0.235f};
            case ItemRarity::Unique:    return {0.937f, 0.267f, 0.267f};
            default: return Color::white();
        }
    }
}

// ============================================================================
// Mob Level Colors (TWOM-style nameplate coloring)
// ============================================================================
namespace MobLevelColors {
    constexpr int TRIVIAL_THRESHOLD = -3;
    constexpr int MAX_DANGER_LEVEL = 4;

    inline Color getColor(int mobLevel, int playerLevel) {
        int diff = mobLevel - playerLevel;
        if (diff <= TRIVIAL_THRESHOLD) return {0.5f, 0.5f, 0.5f};   // Gray
        if (diff < 0)  return {0.290f, 0.871f, 0.502f};              // Green
        if (diff == 0) return Color::white();
        switch (diff) {
            case 1: return {0.376f, 0.647f, 0.980f};  // Blue
            case 2: return {0.659f, 0.333f, 0.969f};  // Purple
            case 3: return {0.984f, 0.573f, 0.235f};  // Orange
            default: return {0.937f, 0.267f, 0.267f}; // Red
        }
    }

    inline bool isTrivial(int mobLevel, int playerLevel) {
        return (mobLevel - playerLevel) <= TRIVIAL_THRESHOLD;
    }
}

