// XP Formula
// ============================================================================
inline int64_t calculateXPToNextLevel(int level) {
    int64_t val = static_cast<int64_t>(std::round(0.35 * std::pow(level, 5.1)));
    return val > 100 ? val : 100;
}

// ============================================================================
// UUID v4 generation for item instance IDs
// ============================================================================
inline std::string generateItemInstanceId() {
    static thread_local std::mt19937_64 rng(std::random_device{}());
    std::uniform_int_distribution<uint64_t> dist;
    uint64_t hi = dist(rng);
    uint64_t lo = dist(rng);
    // Set version 4 (bits 12-15 of time_hi_and_version)
    hi = (hi & 0xFFFFFFFFFFFF0FFFULL) | 0x0000000000004000ULL;
    // Set variant 1 (bits 6-7 of clk_seq_hi_res)
    lo = (lo & 0x3FFFFFFFFFFFFFFFULL) | 0x8000000000000000ULL;
    char buf[37];
    std::snprintf(buf, sizeof(buf),
        "%08x-%04x-%04x-%04x-%012llx",
        static_cast<uint32_t>(hi >> 32),
        static_cast<uint16_t>((hi >> 16) & 0xFFFF),
        static_cast<uint16_t>(hi & 0xFFFF),
        static_cast<uint16_t>(lo >> 48),
        static_cast<unsigned long long>(lo & 0x0000FFFFFFFFFFFFULL));
    return std::string(buf);
}

// ============================================================================
// Replication Visual Encoding
// ============================================================================
enum class MoveState : uint8_t {
    Idle    = 0,
    Walking = 1
};

// Map engine Direction to 3-direction animDir (down=0, up=1, side=2)
inline uint8_t facingToAnimDir(Direction facing) {
    switch (facing) {
        case Direction::Down:  return 0;
        case Direction::Up:    return 1;
        case Direction::Left:  return 2;
        case Direction::Right: return 2;
        case Direction::None:  return 0;
    }
    return 0;
}

// Encode: animId = animDir * 4 + animType
// animType: 0=idle, 1=walk, 2=attack, 3=cast
// Special: 12 = death
inline uint16_t encodeAnimId(uint8_t animDir, uint8_t animType) {
    return static_cast<uint16_t>(animDir * 4 + animType);
}

// Decode animId → animDir (0-2) and animType (0-3)
inline void decodeAnimId(uint16_t animId, uint8_t& animDir, uint8_t& animType) {
    if (animId == 12) { animDir = 0; animType = 4; return; } // death special case
    animDir  = static_cast<uint8_t>(animId / 4);
    animType = static_cast<uint8_t>(animId % 4);
}

// Pack 3 equipment visual indices (10 bits each) into uint32
inline uint32_t packEquipVisuals(uint16_t weaponIdx, uint16_t armorIdx, uint16_t hatIdx) {
    return (static_cast<uint32_t>(weaponIdx) & 0x3FF)
         | ((static_cast<uint32_t>(armorIdx)  & 0x3FF) << 10)
         | ((static_cast<uint32_t>(hatIdx)    & 0x3FF) << 20);
}

// Unpack uint32 → 3 visual indices
inline void unpackEquipVisuals(uint32_t packed, uint16_t& weaponIdx, uint16_t& armorIdx, uint16_t& hatIdx) {
    weaponIdx = static_cast<uint16_t>(packed & 0x3FF);
    armorIdx  = static_cast<uint16_t>((packed >> 10) & 0x3FF);
    hatIdx    = static_cast<uint16_t>((packed >> 20) & 0x3FF);
}

// Pack 6 costume visual indices (10 bits each) into uint64
inline uint64_t packCostumeVisuals(uint16_t weapon, uint16_t armor, uint16_t hat,
                                    uint16_t shield, uint16_t gloves, uint16_t boots) {
    return (static_cast<uint64_t>(weapon)  & 0x3FF)
         | ((static_cast<uint64_t>(armor)  & 0x3FF) << 10)
         | ((static_cast<uint64_t>(hat)    & 0x3FF) << 20)
         | ((static_cast<uint64_t>(shield) & 0x3FF) << 30)
         | ((static_cast<uint64_t>(gloves) & 0x3FF) << 40)
         | ((static_cast<uint64_t>(boots)  & 0x3FF) << 50);
}

// Unpack uint64 -> 6 costume visual indices
inline void unpackCostumeVisuals(uint64_t packed,
                                  uint16_t& weapon, uint16_t& armor, uint16_t& hat,
                                  uint16_t& shield, uint16_t& gloves, uint16_t& boots) {
    weapon = static_cast<uint16_t>(packed & 0x3FF);
    armor  = static_cast<uint16_t>((packed >> 10) & 0x3FF);
    hat    = static_cast<uint16_t>((packed >> 20) & 0x3FF);
    shield = static_cast<uint16_t>((packed >> 30) & 0x3FF);
    gloves = static_cast<uint16_t>((packed >> 40) & 0x3FF);
    boots  = static_cast<uint16_t>((packed >> 50) & 0x3FF);
}

