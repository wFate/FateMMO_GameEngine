// ============================================================================
// XP Formula
// ============================================================================
inline int64_t calculateXPToNextLevel(int level) {
    int64_t val = static_cast<int64_t>(std::round(0.35 * std::pow(level, 5.1)));
    return val > 100 ? val : 100;
}

